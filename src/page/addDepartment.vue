<template>
  <div>
    <head-top></head-top>
    <el-row style="margin-top: 20px;">
      <el-col :span="12" :offset="4">
        <el-form
          :model="formData"
          :rules="rules"
          ref="formData"
          label-width="110px"
          class="demo-formData"
        >
          <el-form-item label="部门编号" prop="departmentId">
            <el-input v-model="formData.departmentId"></el-input>
          </el-form-item>
					<el-form-item label="部门名称" prop="departmentName">
            <el-input v-model="formData.departmentName"></el-input>
          </el-form-item>
          <el-form-item class="button_submit">
            <el-button type="primary" @click="submitForm('formData')">添加部门</el-button>
          </el-form-item>
        </el-form>
      </el-col>
    </el-row>
  </div>
</template>

<script>
import headTop from "@/components/headTop";
export default {
  data() {
    return {
      formData: {
        departmentId: "", //部门编号
        departmentName: "" //部门名称
      },
      rules: {
        departmentId: [
          { required: true, message: "请输入部门编号", trigger: "blur" }
        ],
        departmentName: [
          { required: true, message: "请输入部门名称", trigger: "blur" }
        ]
      }
    };
  },
  components: {
    headTop
  },
  inject: ["reload"],
  mounted() {
    //this.initData();
  },
  methods: {
    submitForm(formName) {
      this.$refs[formName].validate(async valid => {
        if (valid) {
          try {
            let form = new FormData();
            form.append("departmentId", this.formData.departmentId);
            form.append("departmentName", this.formData.departmentName);
            this.$http.post("department/add", form).then(res => {
              if (res.data.code == 1) {
                this.$message({
                  type: "success",
                  message: "添加部门信息成功！"
                });
                this.reload();
              } else {
                this.$message.error("添加部门信息失败！");
              }
            });
          } catch (err) {
            console.log(err);
          }
        } else {
          this.$notify.error({
            title: "错误",
            message: "请检查输入是否正确",
            offset: 100
          });
          return false;
        }
      });
    }
  }
};
</script>

